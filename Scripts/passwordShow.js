function showPassword() {
    const x = document.getElementById("Psw");
    const y = document.getElementById("CPsw");
    if (x.type === "password") {
        x.type = "text";
    } else {
        x.type = "password";
    }
    if (y.type === "password") {
        y.type = "text";
    } else {
        y.type = "password";
    }
}